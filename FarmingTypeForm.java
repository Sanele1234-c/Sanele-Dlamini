    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
          txtFarmingName.setText("");
        txtTypeOfProduct.setText("");

        FarmingType ft = new FarmingType();
        ft.setFarmingCode(this.txtFarmingCode.getText());
       
        if (!(this.txtFarmingCode.getText().isEmpty()))
        {      
              try
              {
                   ResultSet rec = ft.ViewFarmingType(ft.getFarmingCode());
                   while (rec.next())
                   {              
                       this.txtFarmingName.setText(rec.getString(2));
                       this.txtTypeOfProduct.setText(rec.getString(3));
                       this.txtRegionCode.setText(rec.getString(4));
                            
                   }
                   
                   if (!(this.txtFarmingName.getText().isEmpty()))
                         {
                        
                           JOptionPane.showMessageDialog(rootPane, " the farming type exit in the database");  
                         }
                   else
                         {
                             JOptionPane.showMessageDialog(rootPane, " this farming type does  not exist in the system. register him or her first");
                            
                         }
               }   
               catch(Exception ex)
               {
                   JOptionPane.showMessageDialog(rootPane, ex.getMessage()); 
                   
               }
        }
        else
        {
               JOptionPane.showMessageDialog(rootPane, "Make sure farming code is not empty");
        }                   
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
          FarmingType ft = new FarmingType ();
          if (!(this.txtFarmingCode.getText().isEmpty()))
        {
            if(!(this.txtFarmingName.getText().isEmpty()))
            {
                   ft.setFarmingCode(this.txtFarmingCode.getText());
                   ft.deleteFarmingType();
                  
                   txtFarmingCode.setText("");
                   txtFarmingName.setText("");
                   txtTypeOfProduct.setText("");
                 
                    updatetable();
                    
                   
                   if (ft.getFarmingTypeDeleted())
                   {              
                       txtFarmingCode.setText("");
                       txtFarmingName.setText("");
                       txtTypeOfProduct.setText("");
                        updatetable();
                        
                        
                   }
                    }
            else
            {
               JOptionPane.showMessageDialog(rootPane, " CHECK type of farming you want to delete "); 
            }
        }
        else
        {
           JOptionPane.showMessageDialog(rootPane, " CHECK type of farming you want to delete ");
        }      
                                             

    }//GEN-LAST:event_btnDeleteActionPerformed

