    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        AgricultureSpecialist a = new AgricultureSpecialist ();
         if (!(this.txtEmployeeNumber.getText().isEmpty()))
        {
            if (!(this.txtName.getText().isEmpty()))
            {
                if (!(this.txtSurname.getText().isEmpty()))
                {
                    a.setEmployeeNumber(this.txtEmployeeNumber.getText());
                     a.setRegionCode(this.txtRegionCode.getText());
                    a.setAgricName(this.txtName.getText());
                    a.setAgricSurname(this.txtSurname.getText());
                    a.setContactNumber(this.txtCellphoneNumber.getText());
                    a.setContactNumber(this.txtCellphoneNumber.getText());
                    a.setEmailAddress(this.txtEmailAddress.getText());
                    
                    a.AddAgricultureSpecialistDetails();  
                    if (a.getIsAdded())
                    {
                        
                     txtEmployeeNumber.setText("");
                     txtRegionCode.setText("");
                     txtName.setText("");
                     txtSurname.setText("");
                     txtCellphoneNumber.setText("");
                     txtEmailAddress.setText("");
                     
                     updatetable();
                     
                    }
                }
                 else
                {
                    JOptionPane.showMessageDialog(rootPane, "fill all the information required before you register this agricSpec");  
                }
            
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "Make sure agricSpec name is not empty");  
            }          
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, "Make sure agricSpec ID is not empty");
        }                                          

    }//GEN-LAST:event_btnAddActionPerformed

