    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        txtRegionCode.setText("");
         txtSurname.setText("");
        txtName.setText("");
       txtCellphoneNumber.setText("");
        txtEmailAddress.setText("");
        
        
        AgricultureSpecialist a = new AgricultureSpecialist();
        a.setEmployeeNumber(this.txtEmployeeNumber.getText());
       
        if (!(this.txtEmployeeNumber.getText().isEmpty()))
        {      
              try
              {
                   ResultSet rec = a.ViewAgricultureSpecialist(a.getEmployeeNumber());
                   while (rec.next())
                   {    
                       this.txtRegionCode.setText(rec.getString(2));
                       this.txtName.setText(rec.getString(3));
                       this.txtSurname.setText(rec.getString(4));
                       this.txtCellphoneNumber.setText(rec.getString(5));
                       this.txtEmailAddress.setText(rec.getString(6));        
                   }
                   
                   if (!(this.txtName.getText().isEmpty()))
                         {
                        
                           JOptionPane.showMessageDialog(rootPane, " the agricSpec exit in the database");  
                         }
                   else
                         {
                             JOptionPane.showMessageDialog(rootPane, " this agricSpec does  not exist in the system. register him or her first");
                            
                         }
               }   
               catch(Exception ex)
               {
                   JOptionPane.showMessageDialog(rootPane, ex.getMessage()); 
                   
               }
        }
        else
        {
               JOptionPane.showMessageDialog(rootPane, "Make sure employment number is not empty");
        }                                        

    }//GEN-LAST:event_btnViewActionPerformed

